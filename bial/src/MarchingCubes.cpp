  Vector3D MarchingCubes::VertexInterp( float isolevel, const Vector3D &p1, const Vector3D &p2, float valp1,
                                        float valp2 ) {
    try {
      if( std::abs( isolevel - valp1 ) < 0.00001 ) {
        return( p1 );
      }
      if( std::abs( isolevel - valp2 ) < 0.00001 ) {
        return( p2 );
      }
      if( std::abs( valp1 - valp2 ) < 0.00001 ) {
        return( p1 );
      }
      return( p1 + ( p2 - p1 ) * ( isolevel - valp1 ) / ( valp2 - valp1 ) );
/*      return p1 + (p2 - p1)/2.0; */
    }
    catch( std::bad_alloc &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Memory allocation error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( std::runtime_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Runtime error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( const std::out_of_range &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Out of range exception." ) );
      throw( std::out_of_range( msg ) );
    }
    catch( const std::logic_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Logic Error." ) );
      throw( std::logic_error( msg ) );
    }
  }

  Normal MarchingCubes::NormalInterp( float isolevel, int dir1, int dir2, float valp1, float valp2 ) {
    try {
      Normal n1 = getNormal( dir1 );
      Normal n2 = getNormal( dir2 );
      if( std::abs( isolevel - valp1 ) < 0.00001 ) {
        return( n1 );
      }
      if( std::abs( isolevel - valp2 ) < 0.00001 ) {
        return( n2 );
      }
      if( std::abs( valp1 - valp2 ) < 0.00001 ) {
        return( n1 );
      }
      return( n1 + ( n2 - n1 ) * ( isolevel - valp1 ) / ( valp2 - valp1 ) );
    }
    catch( std::bad_alloc &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Memory allocation error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( std::runtime_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Runtime error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( const std::out_of_range &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Out of range exception." ) );
      throw( std::out_of_range( msg ) );
    }
    catch( const std::logic_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Logic Error." ) );
      throw( std::logic_error( msg ) );
    }
  }

  Normal MarchingCubes::getNormal( int dir ) {
    Normal norm;
    for( size_t dms = 0; dms < 3; ++dms ) {
      if( dir & int( std::pow( 2, dms * 2 ) ) ) {
        norm[ dms ] = 1;
      }
      else if( dir & int( std::pow( 2, dms * 2 + 1 ) ) ) {
        norm[ dms ] = -1;
      }
    }
    return( norm );
  }

