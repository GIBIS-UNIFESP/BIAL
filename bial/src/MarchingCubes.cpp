  int MarchingCubes::Polygonize( const Cell &cell,
                                 float isolevel,
                                 Vector< size_t > &tris,
                                 Vector< Point3D > &vertices,
                                 Vector< Normal > &normals ) {
    try {
      std::array< Vector3D, 12 > vertexList;
      if( edgeTable[ cell.idx ] == 0 ) {
        return( 0 );
      }
      getVertexList( vertexList, cell, isolevel );
      std::array< Normal, 12 > normsList;
      getNormalsList( normsList, cell, isolevel );
      int TriangleCount = 0;
#pragma omp critical
      {
        std::array< size_t, 12 > localRemap;
        for( size_t vtx = 0; vtx < 12; ++vtx ) {
          if( edgeTable[ cell.idx ] >> vtx ) {
            localRemap[ vtx ] = vertices.size( );
            vertices.push_back( vertexList[ vtx ].toPoint( ) );
            normals.push_back( normsList[ vtx ] );
          }
        }
        for( size_t vtx = 0; triTable[ cell.idx ][ vtx ] != -1; ++vtx ) {
          tris.push_back( localRemap[ triTable[ cell.idx ][ vtx ] ] );
          if( vtx % 3 == 0 ) {
            TriangleCount++;
          }
        }
      }
      return( TriangleCount );
/*      return 0; */
    }
    catch( std::bad_alloc &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Memory allocation error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( std::runtime_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Runtime error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( const std::out_of_range &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Out of range exception." ) );
      throw( std::out_of_range( msg ) );
    }
    catch( const std::logic_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Logic Error." ) );
      throw( std::logic_error( msg ) );
    }
  }

  void MarchingCubes::getVertexList( std::array< Vector3D, 12 > &vertexList, const Cell &cell, float isolevel ) {
    try {
      /* Find the vertices where the surface intersects the cube */
      if( edgeTable[ ( int ) cell.idx ] & 1 ) {
        vertexList[ 0 ] =
          VertexInterp( isolevel, cell.p[ 0 ], cell.p[ 1 ], cell.val[ 0 ], cell.val[ 1 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 2 ) {
        vertexList[ 1 ] =
          VertexInterp( isolevel, cell.p[ 1 ], cell.p[ 2 ], cell.val[ 1 ], cell.val[ 2 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 4 ) {
        vertexList[ 2 ] =
          VertexInterp( isolevel, cell.p[ 2 ], cell.p[ 3 ], cell.val[ 2 ], cell.val[ 3 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 8 ) {
        vertexList[ 3 ] =
          VertexInterp( isolevel, cell.p[ 3 ], cell.p[ 0 ], cell.val[ 3 ], cell.val[ 0 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 16 ) {
        vertexList[ 4 ] =
          VertexInterp( isolevel, cell.p[ 4 ], cell.p[ 5 ], cell.val[ 4 ], cell.val[ 5 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 32 ) {
        vertexList[ 5 ] =
          VertexInterp( isolevel, cell.p[ 5 ], cell.p[ 6 ], cell.val[ 5 ], cell.val[ 6 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 64 ) {
        vertexList[ 6 ] =
          VertexInterp( isolevel, cell.p[ 6 ], cell.p[ 7 ], cell.val[ 6 ], cell.val[ 7 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 128 ) {
        vertexList[ 7 ] =
          VertexInterp( isolevel, cell.p[ 7 ], cell.p[ 4 ], cell.val[ 7 ], cell.val[ 4 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 256 ) {
        vertexList[ 8 ] =
          VertexInterp( isolevel, cell.p[ 0 ], cell.p[ 4 ], cell.val[ 0 ], cell.val[ 4 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 512 ) {
        vertexList[ 9 ] =
          VertexInterp( isolevel, cell.p[ 1 ], cell.p[ 5 ], cell.val[ 1 ], cell.val[ 5 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 1024 ) {
        vertexList[ 10 ] =
          VertexInterp( isolevel, cell.p[ 2 ], cell.p[ 6 ], cell.val[ 2 ], cell.val[ 6 ] );
      }
      if( edgeTable[ ( int ) cell.idx ] & 2048 ) {
        vertexList[ 11 ] =
          VertexInterp( isolevel, cell.p[ 3 ], cell.p[ 7 ], cell.val[ 3 ], cell.val[ 7 ] );
      }
    }
    catch( std::bad_alloc &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Memory allocation error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( std::runtime_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Runtime error." ) );
      throw( std::runtime_error( msg ) );
    }
    catch( const std::out_of_range &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Out of range exception." ) );
      throw( std::out_of_range( msg ) );
    }
    catch( const std::logic_error &e ) {
      std::string msg( e.what( ) + std::string( "\n" ) + BIAL_ERROR( "Logic Error." ) );
      throw( std::logic_error( msg ) );
    }
  }

