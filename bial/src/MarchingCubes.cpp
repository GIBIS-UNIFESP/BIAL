  void Cell::calcIdx( float isolevel ) {
    idx = 0;
    if( val[ 0 ] < isolevel ) {
      idx |= 1;
    }
    if( val[ 1 ] < isolevel ) {
      idx |= 2;
    }
    if( val[ 2 ] < isolevel ) {
      idx |= 4;
    }
    if( val[ 3 ] < isolevel ) {
      idx |= 8;
    }
    if( val[ 4 ] < isolevel ) {
      idx |= 16;
    }
    if( val[ 5 ] < isolevel ) {
      idx |= 32;
    }
    if( val[ 6 ] < isolevel ) {
      idx |= 64;
    }
    if( val[ 7 ] < isolevel ) {
      idx |= 128;
    }
  }

  void Cell::printIdx( ) {
    std::cout << "idx[" << idx << "] = ";
    for( int i = 0; i < 8; ++i ) {
      if( ( idx >> i ) % 2 ) {
        std::cout << i << " ";
      }
    }
    std::cout << std::endl;
  }

  void Cell::set( size_t idx, float v, int direction, const Vector3D &pos ) {
    val[ idx ] = v;
    dir[ idx ] = direction;
    p[ idx ] = pos;
  }

  void MarchingCubes::printEdges( int idx ) {
    int edges = edgeTable[ idx ];
    std::cout << "edges[" << idx << "] = ";
    for( int i = 0; i < 12; ++i ) {
      if( ( edges >> i ) % 2 ) {
        std::cout << i << " ";
      }
    }
    std::cout << std::endl;
  }

  void MarchingCubes::printTris( int idx ) {
    std::cout << "triTable[" << idx << "] = ";
    for( int i = 0; i < 16; ++i ) {
      std::cout << ( int ) triTable[ idx ][ i ] << " ";
    }
    std::cout << std::endl;
  }


